export type Locale = 'zh' | 'en';

export const DEFAULT_LOCALE: Locale = 'zh';

export function getLocaleFromPathname(pathname: string | null | undefined): Locale {
  return pathname?.startsWith('/en') ? 'en' : DEFAULT_LOCALE;
}

export function getHtmlLang(locale: Locale) {
  return locale === 'en' ? 'en' : 'zh-CN';
}

export function getLanguageHref(pathname: string) {
  if (pathname.startsWith('/en/compress')) return '/compress';
  if (pathname.startsWith('/en')) return '/';
  if (pathname.startsWith('/compress')) return '/en/compress';
  return '/en';
}

